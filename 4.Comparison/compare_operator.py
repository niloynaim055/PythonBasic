"""
Less than (<) operator
"""


def less_than(a, b):
    return a < b


print(less_than(1, 4))

"""
Greater than (>) operator
"""


def greater_than(a, b):
    return a > b


print(greater_than(10, 4))

"""
Greater than or equal (>=) operator
"""


def greater_than_or_equal(a, b):
    return a >= b


print(greater_than_or_equal(10, 10))

"""
Less than or equal (<) operator
"""


def less_than_or_equal(a, b):
    return a <= b


print(less_than_or_equal(10, 100))

"""
Equal (==) operator
"""


def equal(a, b):
    return a == b


print(equal("Dhaka", "dhaka"))

"""
Not equal (!=) operator
"""


def not_equal(a, b):
    return a != b


print(not_equal(10, 101))
